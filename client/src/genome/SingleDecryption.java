package genome;

public class SingleDecryption {

	private String add;
	private String mult;

	public String all() {
		return add + mult;
	}

	public String getAdd() {
		return add;
	}

	public void setAdd(String add) {
		this.add = add;
	}

	public String getMult() {
		return mult;
	}

	public void setMult(String mult) {
		this.mult = mult;
	}

}
